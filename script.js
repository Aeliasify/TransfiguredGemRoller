// ===================== ROLL 3 UNIQUE PER COLOR =====================
function rollMultiple(list, count) {
  const result = [];
  while (result.length < count) {
    const pick = list[Math.floor(Math.random() * list.length)];
    if (!result.includes(pick)) result.push(pick);
  }
  return result;
}

